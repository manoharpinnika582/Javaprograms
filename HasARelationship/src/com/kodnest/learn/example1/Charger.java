package com.kodnest.learn.example1;

public class Charger {
	String color;
	String type;
	String brand;
	public Charger(String color, String type, String brand) {
		super();
		this.color = color;
		this.type = type;
		this.brand = brand;
	}
	public String getColor() {
		return color;
	}
	public String getType() {
		return type;
	}
	public String getBrand() {
		return brand;
	}
	
}
